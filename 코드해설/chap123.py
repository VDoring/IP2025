# numpy 라이브러리를 np라는 별칭으로 가져옵니다. 주로 행렬 연산에 사용됩니다.
import numpy as np
# OpenCV 라이브러리를 cv2라는 별칭으로 가져옵니다. 이미지/비디오 처리에 사용됩니다.
import cv2

# # (이 줄은 주석 처리되어 실행되지 않습니다) 512x512 크기의 검은색 이미지를 생성합니다.
# img = np.zeros((512,512,3), np.uint8)

# 'dog.jpg' 파일의 이미지를 불러와 img 변수에 저장합니다.
img = cv2.imread('dog.jpg')

# img 이미지에 (0,0) 좌표에서 (960,638) 좌표까지 파란색(255,0,0) 선을 두께 5로 그립니다.
img = cv2.line(img,(0,0),(960,638),(255,0,0),5)

# img 이미지에 왼쪽 위 꼭짓점이 (384,0), 오른쪽 아래 꼭짓점이 (510,128)인 초록색(0,255,0) 사각형을 두께 3으로 그립니다.
img = cv2.rectangle(img,(384,0),(510,128),(0,255,0),3)

# img 이미지에 중심이 (447,63)이고 반지름이 63인 빨간색(0,0,255) 원을 두께 10으로 그립니다.
img = cv2.circle(img,(447,63), 63, (0,0,255), 10)

# img 이미지에 중심 (256,256), 장축/단축 (100,50), 회전각 0, 시작각 0, 끝각 90인 파란색(255) 타원을 내부를 채워서(-1) 그립니다.
img = cv2.ellipse(img,(256,256),(100,50),0,0,90,255,-1)

# 글씨를 쓰기 위해 FONT_HERSHEY_SIMPLEX 폰트를 font 변수에 저장합니다.
font = cv2.FONT_HERSHEY_SIMPLEX

# img 이미지의 (10,500) 위치에 'OpenCV' 텍스트를 font, 크기 4, 흰색(255,255,255), 두께 12, 부드러운 라인(LINE_AA)으로 추가합니다.
cv2.putText(img,'OpenCV',(10,500), font, 4,(255,255,255),12,cv2.LINE_AA)

# 'frame'이라는 이름의 창에 최종적으로 수정된 img 이미지를 보여줍니다.
cv2.imshow('frame',img)

# 사용자가 키보드를 누를 때까지 무한정 대기하고, 눌린 키의 아스키 코드 값을 k 변수에 저장합니다.
k = cv2.waitKey(0)

# 만약 눌린 키가 ESC 키(아스키 코드 27)라면,
if k == 27:
    # 열려있는 모든 OpenCV 창을 닫습니다.
    cv2.destroyAllWindows()